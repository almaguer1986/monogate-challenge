function callFn(fmt, name, ...args) {
  if (fmt.rust) return `${args[0]}${name}()`;
  return `${name}(${args.join(", ")})`;
}

function ln(fmt, v) {
  return fmt.rust ? `${v}.ln()` : `${fmt.ln}(${v})`;
}
function expp(fmt, v) {
  return fmt.rust ? `${v}.exp()` : `${fmt.exp}(${v})`;
}

// Build code for known operation+language combos
function buildBody(op, fmt, a = "x", b = "y") {
  const { var: mkVar, ret } = fmt;
  const zero = fmt.rust ? "0f64" : "0.0";
  const one  = fmt.rust ? "1f64" : "1.0";

  switch (op) {
    case "mul": // exl(exl(0,x), eml(y,1)): x*y requires x>0
      return (
        mkVar("_ln_x", `${ln(fmt, a)} ${fmt.lineComment} EXL: ln(x) [1n]`) +
        mkVar("_exp_y", `${expp(fmt, b)} ${fmt.lineComment} EML: exp(y) [1n]`) +
        ret + `${expp(fmt, "_ln_x")} * ${ln(fmt, "_exp_y")} ${fmt.lineComment} EXL: x*y [1n]\n`
      );
    case "sub": // eml(exl(0,x), eml(y,1)): x-y requires x>0
      return (
        mkVar("_ln_x", `${ln(fmt, a)} ${fmt.lineComment} EXL: ln(x) [1n]`) +
        mkVar("_exp_y", `${expp(fmt, b)} ${fmt.lineComment} EML: exp(y) [1n]`) +
        ret + `${expp(fmt, "_ln_x")} - ${ln(fmt, "_exp_y")} ${fmt.lineComment} EML: x-y [1n]\n`
      );
    case "neg": // exl(0, deml(x,1)): -x all domain
      return (
        mkVar("_t", `${expp(fmt, `-${a}`)} ${fmt.lineComment} DEML: exp(-x) [1n]`) +
        ret + `${expp(fmt, zero)} * ${ln(fmt, "_t")} ${fmt.lineComment} EXL: -x [1n]\n`
      );
    case "div": // edl(x,y): x/y
      return ret + `${expp(fmt, ln(fmt, a))} / ${ln(fmt, expp(fmt, b))} ${fmt.lineComment} EDL: x/y [1n]\n`;
    case "recip": // edl(0, eml(x,1)): 1/x
      return (
        mkVar("_t", `${expp(fmt, a)} - ${ln(fmt, one)} ${fmt.lineComment} EML: exp(x) [1n]`) +
        ret + `${expp(fmt, zero)} / ${ln(fmt, "_t")} ${fmt.lineComment} EDL: 1/x [1n]\n`
      );
    case "pow": // eml(exl(ln(n),x), 1): x^n
      return ret + `${expp(fmt, `${ln(fmt, b)} * ${ln(fmt, a)}`)} ${fmt.lineComment} EXL: x^n [3n]\n`;
    case "add": // eal(exl(0,x), eml(y,1)): x+y requires x>0
      return (
        mkVar("_ln_x", `${ln(fmt, a)} ${fmt.lineComment} EXL: ln(x) [1n]`) +
        mkVar("_exp_y", `${expp(fmt, b)} ${fmt.lineComment} EML: exp(y) [1n]`) +
        ret + `${expp(fmt, "_ln_x")} + ${ln(fmt, "_exp_y")} ${fmt.lineComment} EAL: x+y [1n]\n`
      );
    case "ln": // exl(0, x)
      return ret + `${expp(fmt, zero)} * ${ln(fmt, a)} ${fmt.lineComment} EXL: ln(x) [1n]\n`;
    case "exp": // eml(x, 1)
      return ret + `${expp(fmt, a)} - ${ln(fmt, one)} ${fmt.lineComment} EML: exp(x) [1n]\n`;
    default:
      return ret + `0 ${fmt.lineComment} TODO: ${op} construction\n`;
  }
}

function fmtArgs(fmt, ops) {
  const unary = ["neg", "ln", "exp", "recip", "sqrt", "abs"];
  const isUnary = unary.includes(ops);
  if (fmt.argType) {
    return isUnary ? `${fmt.argType} x` : `${fmt.argType} x, ${fmt.argType} y`;
  }
  if (fmt.rust) {
    return isUnary ? "x: f64" : "x: f64, y: f64";
  }
  return isUnary ? "x" : "x, y";
}

/**
 * Generate production-ready code for a SuperBEST operation.
 * @param {string} op — operation name (mul, sub, neg, div, pow, add, ln, exp, recip)
 * @param {string} lang — python | js | glsl | hlsl | rust | c
 * @returns {string} formatted source code
 */
export function exportCode(op, lang) {
  const fmt = LANG_FMT[lang];
  if (!fmt) return `// Language '${lang}' not supported`;
  const entry = TABLE[op];
  if (!entry) return `// Operation '${op}' not in SuperBEST table`;

  const header = fmt.header(op, entry.emlNodes, entry.nodes);
  const args   = fmtArgs(fmt, op);
  const body   = buildBody(op, fmt, "x", "y");
  const domNote = entry.domain !== "all x"
    ? `${fmt.lineComment} Domain: ${entry.domain}\n`
    : "";
  return header + domNote + fmt.fn(op, args, body);
}

