/** Count SuperBEST nodes for a {opName: count} map. */
export function nodeCount(opCounts) {
  let total = 0;
  for (const [op, count] of Object.entries(opCounts)) {
    total += (COSTS[op] ?? 1) * count;
  }
  return total;
}

/** Count EML-baseline nodes for a {opName: count} map. */
export function emlNodeCount(opCounts) {
  let total = 0;
  for (const [op, count] of Object.entries(opCounts)) {
    total += (EML_COSTS[op] ?? 1) * count;
  }
  return total;
}

/** Savings percentage: (1 - optimized/original) × 100 */
export function savings(original, optimized) {
  if (!original) return 0;
  return Math.round((1 - optimized / original) * 100);
}

/** Operator family for a named op. */
export function operatorOf(opName) {
  return FAMILY[opName] ?? "EML";
}

